public class Y11EFindSmallestLetterGreaterThanTarget {
    /*
    if its bigger than all of the element, return the first element
     */
    public char nextGreatestLetter (char[] letters, char target) {
        int n = letters.length;
        int l = 0, h = n - 1;

        while (l <= h) {
            int m = l + (h - l) / 2;
            if (letters[m] <= target) {
                l = m + 1;
            } else {
                h = m - 1;
            }
        }
        return l < n ? letters[l] : letters[0];
    }
}
